class Product {
  final int id;
  int quantity;
  final String image;
  final String title;
  final String subTitle;
  final double price;
  final double rating;

  Product({
    required this.id,
    required this.quantity,
    required this.image,
    required this.title,
    required this.subTitle,
    required this.price,
    required this.rating,
  });
}
